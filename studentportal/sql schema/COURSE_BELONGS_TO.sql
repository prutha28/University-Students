CHANGE COLUMN `COURSE_ID` `COURSE_ID` VARCHAR(10) NOT NULL ,
CHANGE COLUMN `D_ID` `D_ID` VARCHAR(10) NOT NULL ,
ADD PRIMARY KEY (`COURSE_ID`, `D_ID`),
ADD INDEX `COURSE_BELONGS_TO_FK2_idx` (`D_ID` ASC);
