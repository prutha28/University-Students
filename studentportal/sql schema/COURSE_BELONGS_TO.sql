ADD CONSTRAINT `COURSE_BELONGS_TO_FK1`
  FOREIGN KEY (`COURSE_ID`)
  REFERENCES `universitydb`.`COURSE` (`COURSE_ID`)
  ON DELETE NO ACTION
  ON UPDATE NO ACTION,
